package com.metacube.training.EmployeePortal.model;

public class EmployeeSkills 
{
    int employeeCodeId, employeecode, employeeSkillCode;

    public int getEmployeeCodeId() {
        return employeeCodeId;
    }

    public void setEmployeeCodeId(int employeeCodeId) {
        this.employeeCodeId = employeeCodeId;
    }

    public int getEmployeecode() {
        return employeecode;
    }

    public void setEmployeecode(int employeecode) {
        this.employeecode = employeecode;
    }

    public int getEmployeeSkillCode() {
        return employeeSkillCode;
    }

    public void setEmployeeSkillCode(int employeeSkillCode) {
        this.employeeSkillCode = employeeSkillCode;
    }
   
}
